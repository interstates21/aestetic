void	draw_bitmap(t_ed *ed, t_bitmap bitmap, t_v2 v, Uint32 color)
{
	int		i;
	int		j;

	j = -1;
	while (++j < 8)
	{
		i = -1;
		while (++i < 9)
		{
			if ((bitmap.bit[j] >> (8 - i)) & 1)
			{
				sdl_put_pix(&(ed->pixels), v.x + i, v.y + j, color);
			}
		}
	}
}

t_text new_text(char *message, Uint32 color, int scale) {
    t_text t;

    t.chars = init_font();
    t.message = message;
    t.color = color;
    t.scale = scale;

	return (t);
}
