#include "doom_nukem.h"

t_vec3f	v2_to_v3(t_vec2f a)
{
	t_vec3f c;

	c.x = a.x;
	c.y = 0;
	c.z = a.y;
	return (c);
}

t_vec2f	v3_to_v2(t_vec3f a)
{
	t_vec2f c;

	c.x = a.x;
	c.y = a.z;
	return (c);
}
