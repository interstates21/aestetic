#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		i;

	i = 0;
	while (s1[i])
		i++;
	s2 = (char*)malloc(sizeof(*s2) * (i + 1));
	if (s2)
	{
		i = 0;
		while (*s1)
			s2[i++] = *s1++;
		s2[i] = '\0';
	}
	return (s2);
}
